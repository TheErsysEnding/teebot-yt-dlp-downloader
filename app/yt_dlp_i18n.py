# Aktive Sprache (mutable global, gewechselt via set_language)
_current_lang = "de"
_listeners: list[Callable[[], None]] = []


def set_language(code: str) -> None:
    """Wechsle aktive Sprache. Triggert alle registrierten listeners
    sodass die UI sich live updaten kann."""
    global _current_lang
    if code not in T:
        code = "en"
    _current_lang = code
    for cb in _listeners:
        try:
            cb()
        except Exception:
            pass


def get_language() -> str:
    return _current_lang


def register_listener(cb: Callable[[], None]) -> None:
    """UI-Komponente registriert sich um bei Sprach-Wechsel re-translatet
    zu werden."""
    _listeners.append(cb)


def t(key: str, **kwargs) -> str:
    """Translate. Fallback-Kette: aktuelle Sprache -> EN -> DE -> Key."""
    val = T.get(_current_lang, {}).get(key)
    if val is None:
        val = T.get("en", {}).get(key)
    if val is None:
        val = T.get("de", {}).get(key)
    if val is None:
        return key  # Kein Fund — show raw Key (debug-friendly)
    if kwargs:
        try:
            return val.format(**kwargs)
        except (KeyError, IndexError):
            return val
    return val
